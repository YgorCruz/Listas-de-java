class Exercicio27 {
    public static void main(String[] args) {
        int t = 10;
        for (int c = 1; c <= 10; c++) {

            System.out.print(c + ", ");

            for (int j = 1; j <= t; j++) {

                System.out.print(j + " ");

            }
            t--;
            System.out.println();

        }

    }
}