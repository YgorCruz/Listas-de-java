class Exercicio26 {
    public static void main(String[] args) {
        for (int c = 1; c <= 10; c++) {

            System.out.print(c + ", ");

            for (int j = 1; j <= c; j++) {

                System.out.print(j + " ");

            }
            System.out.println();

        }

    }

}